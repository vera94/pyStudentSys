class Assignment:
    def __init__(self, teacher, number, content):
        self.__number = number
        self.__teacher = teacher
        self.__content = content
        self.__target_groups = []

def Homework(Assignment):
    def __init__(self, teacher, number, content, end_date):
        Assignment.__init__(self, number, content)
        self.__end_date = end_date
        self.__points = 0

def Lesson(Assignment):
    def __init__(self, teacher, number, content, topic):
        Assignment.__init__(self, number, content)
        self.__topic = topic 
        
    


    
